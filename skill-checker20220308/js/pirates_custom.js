        $(function () {
            var select = $("#tab1");
            var slider1 = $('<div id="slider1"></div>').insertAfter(select).slider({
                value: 0,
                min: 0,
                max: 5,
                range: "min",
                slide: function (event, ui) {
                    $("#skill-1").val(ui.value);
                    $("#UsedPoints").text(ui.value + $("#slider2").slider("value") + $("#slider3").slider("value") + $("#slider4").slider("value") + $("#slider5").slider("value") + $("#slider6").slider("value") + $("#slider7").slider("value") + $("#slider8").slider("value"));

                    // update SkillCode
                    var skillcode = $("#skill-1").val() + $("#skill-2").val() + $("#skill-3").val() + $("#skill-4").val() + $("#skill-5").val() + $("#skill-6").val() + $("#skill-7").val() + $("#skill-8").val();
                    $("#SkillCode").val(skillcode);

                    if ($("#UsedPoints").text() > 25) {
                        // alert('hallo');
                        $("#Points").css("background-color", "#a52a2a");

                    } else { $("#Points").css("background-color", "#2e905f"); }




                }
            });
        });


